package Dominio;

import java.util.ArrayList;

public class Sistema {
    private ArrayList<Editorial> lEditoriales;
    private ArrayList<Genero> lGeneros;
    private ArrayList<Autor> lAutores;
    private ArrayList<Libro> lLibros;
    private ArrayList<Venta> lVentas;

    public ArrayList<Editorial> getlEditoriales() {
        return lEditoriales;
    }

    public void setlEditoriales(ArrayList<Editorial> lEditoriales) {
        this.lEditoriales = lEditoriales;
    }

    public ArrayList<Genero> getlGeneros() {
        return lGeneros;
    }

    public void setlGeneros(ArrayList<Genero> lGeneros) {
        this.lGeneros = lGeneros;
    }

    public ArrayList<Autor> getlAutores() {
        return lAutores;
    }

    public void setlAutores(ArrayList<Autor> lAutores) {
        this.lAutores = lAutores;
    }

    public ArrayList<Libro> getlLibros() {
        return lLibros;
    }

    public void setlLibros(ArrayList<Libro> lLibros) {
        this.lLibros = lLibros;
    }

    public ArrayList<Venta> getlVentas() {
        return lVentas;
    }

    public void setlVentas(ArrayList<Venta> lVentas) {
        this.lVentas = lVentas;
    }
    

    
    
    
    
    
}


